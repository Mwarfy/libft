#include "libft.h"

void	ft_memdel(void **ap)
{
	int i;

	i = 0;
	while(ap[i] != '\0')
	{
		ap[i] = NULL;
		free(ap[i]);
		i++;
	}
	ap = NULL;
	free(ap);
}
